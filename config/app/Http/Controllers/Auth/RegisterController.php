            // $days .= $slots[$i];
            // DoctorSlot::create([
            //     'doctorId' => $user_id,
            //     'freeslots' => $days,
            // ]);
        } else {
            $user = User::create([
                'name' => $data['name'],
                'email' => $data['email'],
                'password' => Hash::make($data['password']),
                'role' => $data['role'],
            ]);
        }
        
        return $user;
        
    }
}
